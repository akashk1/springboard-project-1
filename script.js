function search(str) {
  let results = [];

  for (let i = 0; i < fruit.length; i++) {
    const lowercaseFruit = fruit[i].toLowerCase();
    const lowercaseStr = str.toLowerCase();

    if (lowercaseFruit.includes(lowercaseStr)) {
      results.push(fruit[i]);
    }
  }

  return results;
}

function searchHandler(e) {
  const inputVal = e.target.value;
  const results = search(inputVal);
  showSuggestions(results, inputVal);
}

function showSuggestions(results, inputVal) {
  if (results.length === 0 || inputVal.length === 0) {
    suggestions.innerHTML = "";
    suggestions.classList.remove("has-suggestions");
    return;
  }

  const suggestionsList = results
    .map((result) => `<li>${result}</li>`)
    .join("");

  console.log(suggestionsList);
  suggestions.innerHTML = suggestionsList;
  suggestions.classList.add("has-suggestions");
}

function useSuggestion(e) {
  if (e.target.tagName === "LI") {
    const selectedSuggestion = e.target.innerText;
    input.value = selectedSuggestion;
  }
}

input.addEventListener("keyup", searchHandler);
suggestions.addEventListener("click", useSuggestion);
